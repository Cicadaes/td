package com.talkingdata.marketing.core.entity.thirdmodel.usercloud;

/**
 * The type Meta attribute.
 * @author xiaoming.kang
 */
public class MetaAttribute {

    private String attributeCode;
    private String attributeName;
    private String objectCode;
    private String objectIdType;
    private String objectType;

    /**
     * Gets attribute code.
     *
     * @return the attribute code
     */
    public String getAttributeCode() {
        return attributeCode;
    }

    /**
     * Sets attribute code.
     *
     * @param attributeCode the attribute code
     */
    public void setAttributeCode(String attributeCode) {
        this.attributeCode = attributeCode;
    }

    /**
     * Gets attribute name.
     *
     * @return the attribute name
     */
    public String getAttributeName() {
        return attributeName;
    }

    /**
     * Sets attribute name.
     *
     * @param attributeName the attribute name
     */
    public void setAttributeName(String attributeName) {
        this.attributeName = attributeName;
    }

    /**
     * Gets object code.
     *
     * @return the object code
     */
    public String getObjectCode() {
        return objectCode;
    }

    /**
     * Sets object code.
     *
     * @param objectCode the object code
     */
    public void setObjectCode(String objectCode) {
        this.objectCode = objectCode;
    }

    /**
     * Gets object id type.
     *
     * @return the object id type
     */
    public String getObjectIdType() {
        return objectIdType;
    }

    /**
     * Sets object id type.
     *
     * @param objectIdType the object id type
     */
    public void setObjectIdType(String objectIdType) {
        this.objectIdType = objectIdType;
    }

    /**
     * Gets object type.
     *
     * @return the object type
     */
    public String getObjectType() {
        return objectType;
    }

    /**
     * Sets object type.
     *
     * @param obejctType the obejct type
     */
    public void setObjectType(String obejctType) {
        this.objectType = obejctType;
    }

}
